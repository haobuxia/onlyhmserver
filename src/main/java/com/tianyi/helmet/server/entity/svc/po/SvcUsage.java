package com.tianyi.helmet.server.entity.svc.po;

/**
 * 服务工单-车辆使用情况
 * Created by liuhanc on 2018/3/16.
 */
public class SvcUsage extends SvcRwhAbstract {
    private String gongkuang;
    private String ranyou;
    private String gaizhuang;
    private String hour;

    public SvcUsage() {
    }

    public SvcUsage(String rwh, String gongkuang, String ranyou, String gaizhuang,String hour) {
        this.rwh = rwh;
        this.gongkuang = gongkuang;
        this.ranyou = ranyou;
        this.gaizhuang = gaizhuang;
        this.hour = hour;
    }

    public String getHour() {
        return hour;
    }

    public void setHour(String hour) {
        this.hour = hour;
    }

    public String getGongkuang() {
        return gongkuang;
    }

    public void setGongkuang(String gongkuang) {
        this.gongkuang = gongkuang;
    }

    public String getRanyou() {
        return ranyou;
    }

    public void setRanyou(String ranyou) {
        this.ranyou = ranyou;
    }

    public String getGaizhuang() {
        return gaizhuang;
    }

    public void setGaizhuang(String gaizhuang) {
        this.gaizhuang = gaizhuang;
    }

    @Override
    public String toString(){
        return "rwh="+rwh+",gongkuang="+gongkuang+",ranyou="+ranyou+",gaizhuang="+gaizhuang;
    }
}
