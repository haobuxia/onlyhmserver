package com.tianyi.helmet.server.entity.data;

import com.tianyi.helmet.server.entity.IdEntity;

import java.time.LocalDateTime;

/**
 * Created by wenxinyan on 2018/9/8.
 */
public class ReferenceValue extends IdEntity {
    private LocalDateTime createTime;
    private String model;
    private String action;
    private String state;
    private int stateUp;
    private int stateLower;

    public LocalDateTime getCreateTime() {
        return createTime;
    }

    public void setCreateTime(LocalDateTime createTime) {
        this.createTime = createTime;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public int getStateUp() {
        return stateUp;
    }

    public void setStateUp(int stateUp) {
        this.stateUp = stateUp;
    }

    public int getStateLower() {
        return stateLower;
    }

    public void setStateLower(int stateLower) {
        this.stateLower = stateLower;
    }
}
