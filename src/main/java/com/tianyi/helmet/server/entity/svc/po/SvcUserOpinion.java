package com.tianyi.helmet.server.entity.svc.po;

/**
 * 任务-用户意见
 * Created by liuhanc on 2018/3/16.
 */
public class SvcUserOpinion extends SvcRwhAbstract {
    private String satisfy;//满意度
    //    private String opinionAudioPath;
    private String userOpinion;

    public SvcUserOpinion() {
    }

    public SvcUserOpinion(String rwh, String satisfy, String userOpinion) {
        this.rwh = rwh;
        this.satisfy = satisfy;
        this.userOpinion = userOpinion;
    }

    public String getSatisfy() {
        return satisfy;
    }

    public void setSatisfy(String satisfy) {
        this.satisfy = satisfy;
    }

    public String getUserOpinion() {
        return userOpinion;
    }

    public void setUserOpinion(String userOpinion) {
        this.userOpinion = userOpinion;
    }

    @Override
    public String toString() {
        return "rwh=" + rwh + ",satisfy=" + satisfy + ",userOpinion=" + userOpinion;
    }
}
