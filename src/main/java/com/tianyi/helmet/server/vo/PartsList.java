package com.tianyi.helmet.server.vo;

import java.io.Serializable;

public class PartsList implements Serializable{
    private static final long serialVersionUID = 1L;
    private String sysOneName;
    private String sysTwoName;
    private String image;
    private String partName;
    private String partPrice;

    public String getSysOneName() {
        return sysOneName;
    }

    public void setSysOneName(String sysOneName) {
        this.sysOneName = sysOneName;
    }

    public String getSysTwoName() {
        return sysTwoName;
    }

    public void setSysTwoName(String sysTwoName) {
        this.sysTwoName = sysTwoName;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getPartName() {
        return partName;
    }

    public void setPartName(String partName) {
        this.partName = partName;
    }

    public String getPartPrice() {
        return partPrice;
    }

    public void setPartPrice(String partPrice) {
        this.partPrice = partPrice;
    }

    @Override
    public String toString() {
        return "PartsList{" +
                "sysOneName='" + sysOneName + '\'' +
                ", sysTwoName='" + sysTwoName + '\'' +
                ", image='" + image + '\'' +
                ", partName='" + partName + '\'' +
                ", partPrice='" + partPrice + '\'' +
                '}';
    }
}
