package com.tianyi.helmet.server.entity.app;

/**
 * apk 文件类型枚举
 *
 * Created by liuhanc on 2017/12/19.
 */
public enum  ApkFileTypeEnum {
    phone("phone","手机"),
    phone_accessible("phone_accessible","手机无障碍"),
    helmet("helmet","头盔"),
    helmet_sensor("helmet_sensor","头盔传感器"),
    helmet_accessible("helmet_accessible","头盔无障碍");

    String code;
    String name;

    public String getCode() {
        return code;
    }

    public String getName() {
        return name;
    }

    private ApkFileTypeEnum(String code, String name){
        this.code = code;
        this.name = name;
    }
}
