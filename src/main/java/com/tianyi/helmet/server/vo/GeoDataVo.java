package com.tianyi.helmet.server.vo;

/**
 *  基于地理位置的数据
 * Created by liuhanc on 2018/1/31.
 */
public class GeoDataVo {
    private String tagName;
    private float lon;
    private float lat;
    private int resId;

    public String getTagName() {
        return tagName;
    }

    public void setTagName(String tagName) {
        this.tagName = tagName;
    }

    public float getLon() {
        return lon;
    }

    public void setLon(float lon) {
        this.lon = lon;
    }

    public float getLat() {
        return lat;
    }

    public void setLat(float lat) {
        this.lat = lat;
    }

    public int getResId() {
        return resId;
    }

    public void setResId(int resId) {
        this.resId = resId;
    }
}
