package com.tianyi.helmet.server.entity.tianyuan;

/**
 * 天远设备信息
 *
 * Created by liuhanc on 2018/1/29.
 */
public class VclInfo {
    private String vcl_no;//设备号
    private String vcl_brand;//设备品牌
    private String vcl_type;//设备型号
    private String vcl_producedate;//出厂日期
    private String vcl_saledate;//销售日期
    private String clnt_name;//客户名称
    private String clnt_mobile;//客户手机号
    private String clnt_adrs;//设备地址

    public String getVcl_no() {
        return vcl_no;
    }

    public void setVcl_no(String vcl_no) {
        this.vcl_no = vcl_no;
    }

    public String getVcl_brand() {
        return vcl_brand;
    }

    public void setVcl_brand(String vcl_brand) {
        this.vcl_brand = vcl_brand;
    }

    public String getVcl_type() {
        return vcl_type;
    }

    public void setVcl_type(String vcl_type) {
        this.vcl_type = vcl_type;
    }

    public String getVcl_producedate() {
        return vcl_producedate;
    }

    public void setVcl_producedate(String vcl_producedate) {
        this.vcl_producedate = vcl_producedate;
    }

    public String getVcl_saledate() {
        return vcl_saledate;
    }

    public void setVcl_saledate(String vcl_saledate) {
        this.vcl_saledate = vcl_saledate;
    }

    public String getClnt_name() {
        return clnt_name;
    }

    public void setClnt_name(String clnt_name) {
        this.clnt_name = clnt_name;
    }

    public String getClnt_mobile() {
        return clnt_mobile;
    }

    public void setClnt_mobile(String clnt_mobile) {
        this.clnt_mobile = clnt_mobile;
    }

    public String getClnt_adrs() {
        return clnt_adrs;
    }

    public void setClnt_adrs(String clnt_adrs) {
        this.clnt_adrs = clnt_adrs;
    }

    @Override
    public String toString(){
        return vcl_brand+"-"+vcl_type+"-"+vcl_no;
    }
}
